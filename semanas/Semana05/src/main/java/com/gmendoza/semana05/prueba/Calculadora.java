public class Calculadora {

    public Calculadora() {
    }
    
    public int Suma(int n1, int n2){
      int resultado;
      resultado = n1+n2;
      return resultado;
    }
    
    public int Resta(int n1, int n2){
      int resultado;
      resultado = n1-n2;
      return resultado;
    }
    
    public int Multiplicacion(int n1, int n2){
      int resultado;
      resultado = n1*n2;
      return resultado;
    }
    
    public int Division(int n1, int n2){
      int resultado;
      resultado = n1/n2;
      return resultado;
    }
}
